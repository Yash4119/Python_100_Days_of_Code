# Virtual Environment in Python
'''
virtual environment is a tool used to isolate python environment on a single machine, allowing you to work on multiple projects with different dependencies and packages without conflicts.
This can be especially useful when working on projects that have conflicting package versions or packages that are not compatible with each one.
'''
'''
1. To create virtual environment
=> python -m venv Virtual_Environment_Name

2. To use a particular virtual environment
=> virtual_environment_name/scripts/activate

3. To deactivate an environment
=> deactivate
'''

# Requirement.txt file
'''
1. Find packages installed on your PC
=> pip freeze

2. move data about packages installed in a file
=> pip freeze > requirements.txt

3. install the packages mentioned in the requirements.txt
=> pip freeze -r requirements.txt

'''
