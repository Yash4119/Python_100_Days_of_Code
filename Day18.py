# While Loops in Python
# Python Do not have Do While Loops

print("increment with 1")
i = 3
while(i<=100):
    print(i)
    i+=1

print("Increment with 5")
i = 5
while(i<=100):
    print(i)
    i+=5

print("Reverse While Loop")
i = int(input("Enter the last number you want"))
while(i>=0):
    print(i)
    i-=1
else:
    print("Yes!! I am Out of the While Loop")